function showInfo(i) {
  // construct character
  var character = "";
  if (DATA[i].codePoint >= 0x10000) {
    codePt = DATA[i].codePoint - 0x10000;
    character = String.fromCharCode(
      0xd800 | (0x3ff & (codePt >> 10)),
      0xdc00 | (0x3ff & codePt)
    );
  } else {
    character = String.fromCharCode(DATA[i].codePoint);
  }
  if (DATA[i].extended) {
    character += String.fromCharCode(0xFE00);
  }
  document.getElementById("detailsLetter").innerText = character;
  // code point(s)
  document.getElementById("detailsCodePoint").innerText = "U+" + DATA[i].codePoint.toString(16).toUpperCase().padStart(4, '0') + (DATA[i].extended ? ", U+FE00" : "")
  // names
  document.querySelector("#detailsNameUnicode > span").innerText = DATA[i].name[0];
  document.querySelector("#detailsNameSchool > span").innerText = DATA[i].name[1];
  document.querySelector("#detailsNameRadio > span").innerText = DATA[i].name[2];
  // pronunciation
  document.querySelector("#detailsPronIPA > span").innerText = DATA[i].pronunciation[0];
  document.querySelector("#detailsPronXSAMPA > code > span").innerText = DATA[i].pronunciation[1];
  document.querySelector("#detailsPronWebster > span").innerHTML = DATA[i].pronunciation[2];
  document.querySelector("#detailsPronExample > span").innerText = DATA[i].pronunciation[3];
  // notes
  let notesList = document.querySelector("#detailsNotesContent > ul");
  notesList.innerHTML = "";
  for (let j = 0; j < DATA[i].notes.length; j++) {
    let item = document.createElement("li");
    item.innerHTML = DATA[i].notes[j];
    notesList.appendChild(item);
  }
}

document.addEventListener("DOMContentLoaded", () => {
  // add click listener to all letters
  let letters = document.querySelectorAll(".letter");
  for (let i = 0; i < letters.length; i++) {
    letters[i].addEventListener("click", () => {
      showInfo(i);
    });
  }
})
